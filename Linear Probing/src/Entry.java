public class Entry {

    private int key;
    private int value;

    /* Set key method */
    public void setKey(int key) {
        this.key = key;
    }

    /* Set value method */
    public void setValue(int value) {
        this.value = value;
    }

    /* Get key method */
    public int getKey() {
        return key;
    }

    /* Get value method */
    public int getValue() {
        return value;
    }
} // End Entry class
